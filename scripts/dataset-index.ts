/**
 * InsightPM AI - Dataset Index Generator
 * 运行: npx tsx scripts/dataset-index.ts
 *
 * 扫描 training-data/ 生成数据集索引。
 */

import * as fs from "fs";
import * as path from "path";

const PROJECT_ROOT = path.join(__dirname, "..");
const TRAINING_DIR = path.join(PROJECT_ROOT, "training-data");
const MANIFESTS_DIR = path.join(TRAINING_DIR, "manifests");

interface DatasetEntry {
  caseName: string;
  status: string;
  rawCount: number;
  normalizedCount: number;
  clusterCount: number;
  hardValidationScore: number;
  semanticScore: number;
  baselineType: string;
  acceptedAt: string | null;
}

interface DatasetIndex {
  generatedAt: string;
  totalDatasets: number;
  acceptedCount: number;
  rejectedCount: number;
  totalFeedbacks: number;
  datasets: DatasetEntry[];
}

function main() {
  console.log("=".repeat(60));
  console.log("Dataset Index Generator");
  console.log("=".repeat(60));

  // 确保 manifests 目录存在
  fs.mkdirSync(MANIFESTS_DIR, { recursive: true });

  if (!fs.existsSync(MANIFESTS_DIR)) {
    console.error("No manifests directory found. Run promote first.");
    process.exit(1);
  }

  const manifestFiles = fs.readdirSync(MANIFESTS_DIR).filter(f => f.endsWith(".json"));

  if (manifestFiles.length === 0) {
    console.log("No manifest files found. Generating empty index.");
  }
  const datasets: DatasetEntry[] = [];
  let totalFeedbacks = 0;
  let acceptedCount = 0;
  let rejectedCount = 0;

  for (const file of manifestFiles) {
    const manifest = JSON.parse(fs.readFileSync(path.join(MANIFESTS_DIR, file), "utf-8"));

    const entry: DatasetEntry = {
      caseName: manifest.caseName,
      status: manifest.status,
      rawCount: manifest.rawCount,
      normalizedCount: manifest.normalizedCount,
      clusterCount: manifest.clusterCount,
      hardValidationScore: manifest.hardValidationScore,
      semanticScore: manifest.semanticScore,
      baselineType: manifest.baselineType,
      acceptedAt: manifest.acceptedAt,
    };

    datasets.push(entry);

    if (manifest.status === "accepted") {
      acceptedCount++;
      totalFeedbacks += manifest.normalizedCount;
    } else {
      rejectedCount++;
    }
  }

  // Sort by acceptedAt descending
  datasets.sort((a, b) => {
    if (!a.acceptedAt && !b.acceptedAt) return 0;
    if (!a.acceptedAt) return 1;
    if (!b.acceptedAt) return -1;
    return b.acceptedAt.localeCompare(a.acceptedAt);
  });

  const index: DatasetIndex = {
    generatedAt: new Date().toISOString(),
    totalDatasets: datasets.length,
    acceptedCount,
    rejectedCount,
    totalFeedbacks,
    datasets,
  };

  // Save JSON index
  const indexPath = path.join(TRAINING_DIR, "dataset-index.json");
  fs.writeFileSync(indexPath, JSON.stringify(index, null, 2), "utf-8");
  console.log(`  JSON index: ${indexPath}`);

  // Generate MD index
  const mdPath = path.join(TRAINING_DIR, "dataset-index.md");
  const md = generateMarkdown(index);
  fs.writeFileSync(mdPath, md, "utf-8");
  console.log(`  MD index: ${mdPath}`);

  // Print summary
  console.log("");
  console.log("=".repeat(60));
  console.log(`Total datasets: ${index.totalDatasets}`);
  console.log(`  Accepted: ${index.acceptedCount}`);
  console.log(`  Rejected: ${index.rejectedCount}`);
  console.log(`  Total training feedbacks: ${index.totalFeedbacks}`);
  console.log("=".repeat(60));
}

function generateMarkdown(index: DatasetIndex): string {
  const lines: string[] = [];

  lines.push("# Training Dataset Index");
  lines.push("");
  lines.push(`**Generated:** ${index.generatedAt}`);
  lines.push(`**Total Datasets:** ${index.totalDatasets}`);
  lines.push(`**Accepted:** ${index.acceptedCount}`);
  lines.push(`**Rejected:** ${index.rejectedCount}`);
  lines.push(`**Total Training Feedbacks:** ${index.totalFeedbacks}`);
  lines.push("");

  lines.push("## Accepted Datasets");
  lines.push("");
  lines.push("| Dataset | Raw | Normalized | Clusters | Hard Score | Semantic Score | Baseline | Accepted |");
  lines.push("| --- | --- | --- | --- | --- | --- | --- | --- |");

  const accepted = index.datasets.filter(d => d.status === "accepted");
  for (const d of accepted) {
    const acceptedDate = d.acceptedAt ? new Date(d.acceptedAt).toISOString().split("T")[0] : "-";
    lines.push(`| ${d.caseName} | ${d.rawCount} | ${d.normalizedCount} | ${d.clusterCount} | ${d.hardValidationScore}/100 | ${d.semanticScore}/100 | ${d.baselineType} | ${acceptedDate} |`);
  }

  const rejected = index.datasets.filter(d => d.status === "rejected");
  if (rejected.length > 0) {
    lines.push("");
    lines.push("## Rejected Datasets");
    lines.push("");
    lines.push("| Dataset | Hard Score | Semantic Score | Baseline | Status |");
    lines.push("| --- | --- | --- | --- | --- |");
    for (const d of rejected) {
      lines.push(`| ${d.caseName} | ${d.hardValidationScore}/100 | ${d.semanticScore}/100 | ${d.baselineType} | REJECTED |`);
    }
  }

  lines.push("");
  lines.push("---");
  lines.push("*Auto-generated by `npm run insightpm:dataset-index`*");

  return lines.join("\n");
}

main();
